	USR_SYSID,
	CTRL_SYSID,
	SETTLE_DT,
	PYMT_ID,
	TOTAL_COMM_PD,
	TOTAL_TAX_COLLCTD,
	TOTAL_SERV_FEE_PYMT,
	TAX_COLLCTD,
	TOTAL_PYMT,
	PLN_SRF_SYSID,
	CONVERT(DATE, GETDATE()) AS MD_LOADDATE,
	'INVESTIA-UNIVERIS' AS MD_SRCSYSTEM
FROM
	dbo.COM_SRF (nolock)
WHERE YEAR(TO_DT)= YEAR(GETDATE()) AND MONTH(TO_DT)= MONTH(GETDATE())-1;