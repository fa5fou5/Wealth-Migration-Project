	SRF_SYSID,
	NULLIF(REPLACE(REPLACE(REPLACE(COM_STATUS, CHAR(13), ''), CHAR(10), ''),'|','\|'), '') AS COM_STATUS,
	ACT_SYSID,
	IVD_SYSID,
	AVG_MV,
	FROM_DT,
	TO_DT,
	SRV_FEE_RATE,
	COM_PYBL,
	DLR_SYSID,
	RGN_SYSID,
	BRN_SYSID,
	REP_SYSID,
	NULLIF(REPLACE(REPLACE(REPLACE(CURRENCY_CD, CHAR(13), ''), CHAR(10), ''),'|','\|'), '') AS CURRENCY_CD,
	NULLIF(REPLACE(REPLACE(REPLACE(FAS_FILE_NAME, CHAR(13), ''), CHAR(10), ''),'|','\|'), '') AS FAS_FILE_NAME,
