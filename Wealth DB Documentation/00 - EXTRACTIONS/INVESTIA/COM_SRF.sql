	NULLIF(REPLACE(REPLACE(REPLACE(IVT_TYPE, CHAR(13), ''), CHAR(10), ''),'|','\|'), '') AS IVT_TYPE,
	COM_PAY_PER,
	TAX_YEAR,
	EXCH_DT,
